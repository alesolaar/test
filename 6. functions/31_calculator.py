def add(a,b):
    answer = a + b
    return answer

def substract(a,b):
    answer = a - b
    return answer

def multiply(a,b):
    answer = a * b
    return answer

def divide(a,b):
    answer = a / b
    return answer

def exp(a,b):
    answer = a ** b
    return answer

print(add(2, 3))        
print(substract(2, 3))  
print(multiply(2, 3))   
print(divide(2, 3))     
print(exp(2, 3)) 